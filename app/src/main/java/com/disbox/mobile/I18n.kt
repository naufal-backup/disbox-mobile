    fun t(lang: String, key: String, params: Map<String, String>? = null): String {
        var text = translations[lang]?.get(key) ?: translations["en"]?.get(key) ?: key
        params?.forEach { (k, v) ->
            text = text.replace("{$k}", v)
        }
        return text
    }
